    void showdata(){
       Connection con = konek.getConnection();
       Statement st;
       ResultSet rs;
       String query="SELECT * FROM admin";
       model=(DefaultTableModel) table.getModel();
       try{
           st=con.createStatement();
           rs=st.executeQuery(query);
           model.setRowCount(0);
           Object[] row = new Object[7];
           while(rs.next()){
               row[0]=rs.getString("id_laundry");
               row[1]=rs.getString("nama_pelanggan");
               row[2]=rs.getString("tgl_masuk");
               row[3]=rs.getString("tgl_keluar");
               row[4]=rs.getString("berat");
               row[5]=rs.getString("jenispakaian");
               row[6]=rs.getInt("harga");
               model.addRow(row);
           }
       }catch(SQLException e){
           e.printStackTrace();
       }
    }
    public void pilih () throws ParseException {
        int i = table.getSelectedRow();
        if(i == -1){
            return;
        }
        idlaundry.setText(""+model.getValueAt(i, 0));
        namapelanggan.setText(""+model.getValueAt(i, 1));
        tglmasuk.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(String.valueOf(model.getValueAt(i, 2))));
        tglkeluar.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(String.valueOf(model.getValueAt(i, 3))));
        berat.setText(""+model.getValueAt(i, 4));
        jenispakaian.setSelectedItem(""+model.getValueAt(i, 5));
    }
    
