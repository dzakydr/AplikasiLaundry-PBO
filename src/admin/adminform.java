    private void jenispakaianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jenispakaianActionPerformed
        // TODO add your handling code here:
        String jumlahberat = this.berat.getText();
        int hargacampur = 4000;
        int hargaselimut = 15000;
        int hargaseprai = 10000;
        int hargajacket = 6000;
        double a = Double.parseDouble(jumlahberat);
        
        if(jenispakaian.getSelectedItem() == "Campur"){
            int c = (int) (a * hargacampur);
            harga.setText(""+c);
            
        } else if(jenispakaian.getSelectedItem() == "Selimut"){
            int c = (int) (a * hargaselimut);
            harga.setText(""+c);
        } else if(jenispakaian.getSelectedItem() == "Seprai"){
            int c = (int) (a * hargaseprai);
            harga.setText(""+c);
        } else if(jenispakaian.getSelectedItem() == "Jacket"){
            int c = (int) (a * hargajacket);
            harga.setText(""+c);
        }
        
    }//GEN-LAST:event_jenispakaianActionPerformed

    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        tglmasuk.getDate();
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String query = "INSERT INTO admin (nama_pelanggan, tgl_masuk, tgl_keluar, id_laundry, berat, jenispakaian, harga)"
                + " VALUES ('"
                + namapelanggan.getText() + "','" 
                + date_format.format(tglmasuk.getDate()) + "','"
                + date_format.format(tglkeluar.getDate()) + "','"
                + idlaundry.getText() + "','"
                + Integer.parseInt(berat.getText()) + "','"
                + jenispakaian.getSelectedItem() + "','"
                + Integer.parseInt(harga.getText()) + "')";
        dbtubes.isiData(query);
        JOptionPane.showMessageDialog(null, "Data Berhasil ditambahkan");
        showdata();
    }//GEN-LAST:event_inputActionPerformed

